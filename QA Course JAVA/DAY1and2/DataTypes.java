package DAY1and2;
public class DataTypes {
    public void noReturn() {
        System.out.println("No value returned");
    }

    public String withReturn() {
        System.out.println("Value returned");
        return "Hello World";
    }
}
// return returns a value produced by a method//
// strongly typed = explicit with what can and cannot be returned //
/// void means no return, "main" is the core method within a java app//